			<h5 class="title-d" >Sejarah</h5>
				<p align="justify"><b>BMW </b>mengawali bisnisnya setelah restrukturisasi dari perusahaan pembuat mesin pesawat 
				terbang Rapp Motorenwerke tahun 1917. Akhir dari Perang Dunia I tahun 1918, BMW dipaksa untuk berhenti 
				memproduksi mesin pesawat terbang karena adanya Perjanjian Versailles.[2] Perusahaan ini beralih untuk 
				memproduksi sepeda motor tahun 1923 setelah perjanjian itu mulai dilonggarkan,[3] dan mulai memproduksi 
				mobil tahun 1928/29.[4][5][6]<br>
			    Tahun 1992, BMW mengakuisisi perusahaan studio desain industri di California DesignworksUSA, dan mengakuisisi 
			    penuh tahun 1995. Tahun 1994, BMW membeli perusahaan otomotif Inggris Grup Rover[7] (dimana pada saat itu ada 
			    merek Rover, Land Rover dan MG dan juga hak atas merek yang sudah tidak lagi diproduksi yaitu Austin dan Morris)
			    dan memilikinya selama 6 tahun. Tahun 2000, Rover mengalami kerugian besar dan BMW pun menjualnya. Merek MG dan 
			    Rover dijual ke Phoenix Consortium untuk membentuk MG Rover, sedangkan Land Rover diambil alih Ford. BMW akhirnya
			    mendirikan merek sendiri yang ia namai MINI, yang diluncurkan tahun 2001.<br>
