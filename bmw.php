			  <p align="justify">	<b>BMW</b> (singkatan untuk Bayerische Motoren Werke, atau dalam Bahasa Inggris, Bavarian 
			  Motor Works), adalah sebuah perusahaan otomotif Jerman yang memproduksi mobil dan sepeda motor. BMW didirikan 
			  pada tahun 1916 oleh Franz Josef Popp. BMW AG adalah perusahaan induk dari merk mobil MINI dan Rolls-Royce, 
			  dan, dulunya Rover. BMW dikenal sebagai salah satu perusahaan mobil mewah dengan performa tinggi, dan juga 
