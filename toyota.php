              <h5 class="title-d">Sejarah</h5>
			  <p align="justify">	<b>Toyota Motor Corporation</b> didirikan pada September 1933 sebagai divisi mobil Pabrik 
			  Tenun Otomatis Toyoda. Divisi mobil perusahaan tersebut kemudian dipisahkan pada 27 Agustus 1937 untuk 
			  menciptakan Toyota Motor Corporation seperti saat ini.
				Berangkat dari industri tekstil, Perusahaan yang memproduksi 1 mobil tiap 50 menit ini ternyata 
				menggunakan penamaan Toyota lebih karena penyebutannya lebih enak daripada memakai nama keluarga
				pendirinya, Toyoda. Inilah beberapa tonggak menarik perjalanan Toyota.
				Toyota merupakan pabrikan mobil terbesar di dunia dalam unit sales dan net sales. Pabrikan terbesar 
				di Jepang ini menghasilkan 8-8,5 juta unit mobil di seluruh dunia tiap tahunnya.
				Dibandingkan dengan industri-industri otomotif lain yang menggunakan nama pendirinya sebagai merek
				dagang seperti Honda yang didirikan oleh Soichiro Honda, Daimler-Benz (Gottlieb Daimler dan Karl Benz), 
				Ford (Henry Ford), nama Toyoda tidaklah dipakai sebagai merek. Karena berangkat dari pemikiran sederhana dan 
				visi waktu itu, penyebutan Toyoda kurang enak didengar dan tidak akrab dikenal sehingga diplesetkan menjadi 
				Toyota.<br>
				Sakichi Toyoda lahir pada bulan Februari 1867 di Shizuoka, Jepang. Pria ini dikenal sebagai penemu sejak 
				berusia belasan tahun. Toyoda mengabdikan hidupnya mempelajari dan mengembangkan perakitan tekstil. Dalam 
				usia 30 tahun Toyoda menyelesaikan mesin tenun. Ini kemudian mengantarnya mendirikan cikal bakal perakitan 
				Toyota, yakni Toyoda Automatic Loom Works, Ltd. pada November 1926.
				Di sini hak paten mesin tekstil otomatisnya kemudian dijual kepada Platt Brothers & Co, Ltd. dari Inggris, 
				Britania Raya. Hasil penjualan paten ini, dijadikan modal pengembangan divisi otomotif. Mulai tahun 1933, 
				ketika Toyoda membangun divisi otomotif, tim yang kemudian banyak dikendalikan oleh anaknya Kiichiro Toyoda, 
				tiada henti menghasilkan inovasi-inovasi terdepan di zamannya. Mesin Tipe A berhasil dirampungkan pada 1934. 
				Setahun kemudian mesin ini dicangkokkan prototipe pertama mobil penumpang mereka, A1. Divisi otomotif Toyoda 
				juga menghasilkan truk model G1.<br>
				
				Sejalan makin mengglobalnya produk Toyota, mereka sadar tidak mempunyai grafik logo. Bahkan di Indonesia
				dijumpai kendaraan bermerk Toyota seperti Toyota Kijang dengan logo TOYOTA pada grill di bagian bonnet 
				(hidung) mobil. Pada tahun 1989 Toyota akhirnya memutuskan untuk membuat dua lingkaran oval (elips) yang 
				menghasilkan huruf T dan ellips ketiga mengisyaratkan akan the spirit of understanding in design. Lingkaran
				ketiga itu sekaligus mengelilingi kedua lingkaran ellips sebelumnya yang berbentuk T itu sebagai bukti 
				menjaga dan memengaruhi sekelilingnya.
				Pada tahun 1990-an, Toyota semakin membuktikan bahwa mobil Jepang dapat bersaing dengan mobil Eropa dan 
				Amerika. Toyota Celica berhasil menjadi juara rally dunia, dan Toyota Camry menjadi mobil paling laris
				di Amerika.
				Tahun 1999 Toyota mengakuisisi 51,19% saham Daihatsu dan pada tahun 2001 Toyota membeli 50,11% saham
				Hino.
