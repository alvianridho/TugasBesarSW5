			  <h5 class="title-d" >Deskripsi Singkat</h5>
			  <p align="justify">	<b>Dr. Ing. h.c. F. Porsche AG, </b>atau lebih dikenal sebagai Porsche AG (pengucapan bahasa Jerman: 
			  [ˈpɔʁʃə] ( simak)[3]), adalah sebuah produsen otomotif asal Jerman yang berspesialisasi di produksi mobil sport, SUV, dan 
			  sedan. Porsche AG berkantor pusat di Stuttgart, dan merupakan anak usaha dari Volkswagen AG, yang juga merupakan anak usaha 
			  dari Porsche Automobil Holding SE. Mobil hasil produksi Porsche antara lain Porsche 911, Porsche Boxster, Porsche Cayman, 
			  Porsche Panamera, Porsche Cayenne, Porsche Macan, dan Porsche 918.<br>
			<h5 class="title-d" >Sejarah</h5>
				<p align="justify"><b>Asal mula</b><br>
				Insiyur Austria, Ferdinand Porsche mendirikan "Dr. Ing. h. c. F. Porsche GmbH" di Austria pada tahun 1931,[4] 
				dan kemudian memindahkan kantor pusatnya ke pusat kota Stuttgart. Awalnya, perusahaan ini bergerak di bidang 
				konsultasi pengembangan dan pengerjaan mobil,[4] dan tidak merakit mobil dibawah namanya sendiri. Salah satu 
				tugas pertama perusahaan ini, datang dari pemerintah Jerman yang ingin memproduksi mobil rakyat berjuluk 
				"Volkswagen".[4] Perusahaan ini kemudian menghasilkan Volkswagen Beetle, yang merupakan salah satu desain
				mobil tersukses sepanjang sejarah.[5] Porsche 64 yang dikembangkan pada tahun 1939 pun menggunakan banyak
				komponen dari Beetle.<br>
			    Selama Perang Dunia II,[6] produksi Volkswagen diubah ke versi militer, 
				yang disebut Kübelwagen,[6] dan Schwimmwagen.[6][7] Porsche juga mengerjakan beberapa desain tank, namun kalah
				dengan desain dari Henschel & Son. Walaupun begitu, desain ini nantinya tetap digunakan Porsche untuk mendesain
				penghancur tank, Elefant. Porsche juga mengembangkan tank Panzer VIII Maus di periode akhir perang.<br>
				 Pada akhir Perang Dunia II tahun 1945, pabrik Volkswagen di KdF-Stadt jatuh ke tangan Inggris. Ferdinand pun 
				kehilangan posisi direkturnya, dan Ivan Hirst, seorang Mayor Angkatan Darat Inggris, pun ditugaskan memimpin 
				Porsche.[9] Pada tanggal 15 Desember 1945, Ferdinand dipenjara, dan anaknya, Ferry Porsche pun memutuskan
				untuk merakit mobilnya sendiri, karena ia tidak dapat menemukan mobil yang sesuai dengan seleranya. Ia juga 
				harus memimpin perusahaannya hingga ayahnya dibebaskan pada bulan Agustus 1947.[10] Mobil pertama Porsche 
				adalah Porsche 356 yang dirakit di Gmünd, Austria.[10] Mobil ini lalu mulai diproduksi secara massal oleh
				Porsche Konstruktionen GesmbH, yang didirikan oleh Ferry dan Louise Piëch. Setelah diambil alih oleh ayahnya 
				pada tahun 1950, Porsche pun menunjuk Reutter Karosserie, untuk memproduksi rangka mobil tersebut. Pada tahun 
				1952, Porsche pun membangun pabrik baru di dekat pabrik Reutter Karosserie.[11]<br>
