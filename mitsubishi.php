               <h5 class="title-d" >Sejarah Mitsubishi</h5>
			  <p align="justify">	<b>SPerusahaan Mitsubishi</b> pertama kali didirikan sebagai perusahaan pelayaran oleh Yataro Iwasaki (1834-1885) pada tahun 1870. Pada tahun 1873, namanya diubah menjadi Mitsubishi Shokai (三菱 商会). Nama Mitsubishi (三菱) terdiri dari dua kata: "Mitsu" yang berarti "tiga" dan "hishi" (yang menjadi "bishi" yang berarti di bawah "rendaku"), dan karenanya terdapat tiga buah belah ketupat, yang tecermin dalam logonya yang terkenal. Diartikan pula sebagai "tiga berlian".
				Mitsubishi telah didirikan pada tahun 1870, dua tahun setelah Restorasi Meiji, dengan jasa pengiriman
				sebagai bisnis inti. Hal ini ditandai dengan mulainya pertambangan untuk mendapatkan batu bara yang
				dibutuhkan untuk kapal, pembuatan kapal dari pemerintah untuk memperbaiki kapal yang digunakan, mendirikan 
				sebuah pabrik besi untuk memasok besi ke pembuatan kapal, memulai bisnis asuransi laut untuk melayani
				pengiriman, dan sebagainya. Kemudian, sumber daya manajerial dan kemampuan teknologi yang diperoleh 
				melalui pengoperasian kapal yang digunakan untuk memperluas usaha lebih lanjut ke pembuatan pesawat 
				dan peralatan. Demikian pula, pengalamannya dalam hal pelayaran luar negeri mengakibatkan perusahaan 
				ini masuk ke bisnis perdagangan.<br>
				Perusahaan ini memulai pertambangan batu bara pada 1881 dengan membeli tambang Takashima dan Pulau Hashima
				pada tahun 1890, untuk produksi besar bahan bakar armada kapal uap. Perusahaan ini juga melakukan 
				diversifikasi pada pembuatan kapal, perbankan, asuransi, pergudangan, dan perdagangan. Kemudian 
				diversifikasi juga dilakukan ke dalam sektor-sektor seperti kertas, baja, kaca, peralatan listrik,
				pesawat, minyak, dan real estate. Mitsubishi sebagai perusahaan berbasis luas, memainkan peran penting
				dalam modernisasi industri Jepang.<br>
				Diversifikasi ini akhirnya menghasilkan tiga anak perusahaan. Mitsubishi Bank (sekarang bagian dari 
				Mitsubishi UFJ Financial Group) didirikan pada tahun 1919. Setelah pecah dengan Bank of Tokyo pada tahun 
				1996, dan UFJ Holdings pada tahun 2004, Mitshubishi menjadi bank terbesar di Jepang.<br>
				</p>
