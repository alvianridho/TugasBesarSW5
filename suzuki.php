			  Setelah kembali dari WWII membeli peluang investasi dalam industri transportasi. Selama 1950 Suzuki rekayasa dari
			  beberapa model sepeda motor perintis dunia kompak dan aman. Keberhasilan ini segera motor menyebabkan pembukaan 
			  Suzuki Motor Corporation pada tahun 1955. Tahun ini juga melihat kembali masuknya ke dalam mobil Suzuki industri 
			  'dengan Suzulight. Suzulight di Jepang adalah pertama-mobil ringan. Keberhasilan ini akan membawa pada tahun. 
			  Selama tahun 1980-an, Suzuki memasuki pasar Amerika dengan revolusioner Olahraga Utility Vehicles (SUV) yang 
			  captivated American konsumen. Hari ini, Suzuki adalah perusahaan multinasional yang terus pelopor teknologi 
			  otomotif. <br>
			  Kendaraan mereka saat ini line-up terdiri dari: XL7, Grand Vitara, SX4, Forenza dan Reno. Berbagai 
			  model conceptualized adalah untuk melayani berbagai kebutuhan dan keinginan pemilik mobil di Amerika.
			  Pada tahun 2007, Suzuki mencatat penjualan terbaik tahun perusahaan. Dijual suzuki sebuah 101.884 kendaraan di 
			  Amerika, yang ditandai dengan peningkatan signifikan persentase penjualan berbagai model. Selain itu, Suzuki juga 
			  telah sukses garnered terkenal di Dunia Rally Championship (WRC). 2007 Suzuki melihat perlombaan tim memutuskan 
			  untuk merekam tercepat mendaki bukit di Pikes Peak Hill Club. Suzuki juga membentuk garis halus balap di bawah 
			  katalog produk Suzuki Sport.<br>
			  Sementara Suzuki Sport menawarkan performa tambahan dan pilihan, Suzuki juga teknisi pemeliharaan dan penggantian 
			  komponen. Suzuki bagian melanjutkan perusahaan warisan perusahaan telah ditetapkan dalam industri otomotif. 
			  Suzuki terus suplai mereka berkepentingan dengan negara-of-the-art cadang untuk pemeliharaan, peningkatan dan 
			  penggantian.<br>
