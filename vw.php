			  <h5 class="title-d" >Deskripsi Singkat</h5>
			  <p align="justify">	<b>Volkswagen (biasa disingkat VW)</b> adalah sebuah pabrikan otomotif berbasis di Wolfsburg, 
			  Lower Saxony, Jerman. Volkswagen didirikan oleh Serikat Buruh Jerman (Deutsche Arbeitsfront) pada tahun 1937.
			  Volkswagen merupakan merk asli dari Grup Volkswagen, yang juga membawahi beberapa merk mobil lain seperti Audi, 
			  Bentley Motors,Bugatti Automobiles, Automobili Lamborghini, SEAT, Škoda Auto dan sebuah pabrikan kendaraan 
			  komersial Scania. Tahun 2009, Supervisory Board dari Volkswagen AG mengesahkan pendirian sebuah grup otomotif 
			  yang terintegrasi dengan Porsche di bawah naungan Volkswagen.<br>
			  Dalam bahasa Jerman, "Volkswagen" berarti "mobil rakyat". Tagline Volkswagen saat ini adalah Das Auto 
			  (dalam bahasa Inggris The Car). Moto sebelumnya adalah Aus Liebe zum Automobil (artinya: Kecintaan kepada Mobil).
			  Saat ini Volkswagen mempunyai 3 mobil yang masuk di daftar 10 mobil paling laris sepanjang sejarah menurut 
			  situs web 24/7 Wall St: Volkswagen Golf, Volkswagen Beetle, dan Volkswagen Passat. Dengan 3 mobil ini, 
			  Volkswagen mempunyai jumlah mobil terbanyak dalam daftar itu yang sampai saat ini masih diproduksi.[1]<br>
			<h5 class="title-d" >Sejarah</h5>
				<p align="justify"><b>Latar Belakang Munculnya Volkswagen</b><br>
				Model dari Porsche Type 12 (Zündapp), Museum Industrial Culture, Nurnberg
				Volkswagen awalnya didirikan pada tahun 1937 oleh serikat dagang Nazi, yaitu Serikat Buruh Jerman 
				(Deutsche Arbeitsfront).[2] Sampai pada awal 1930-an pabrikan otomotif Jerman masih banyak terdiri dari 
				pabrikan mobil mewah, dan rata-rata orang Jerman tidak bisa membeli lebih daripada sebuah motor. Melihat 
				sebuah pasar potensial, beberapa pabrikan mobil membuat proyek mereka sendiri-sendiri untuk “mobil rakyat” 
				seperti Mercedes 170H, Adler AutoBahn, Steyr 55, Hanomag 1.3L, dan lainnya. Tren ini sebenarnya bukanlah tren 
				baru, karena Béla Barényi sudah tercatat sebagai pendesain pertama "mobil rakyat" di pertengahan 1920-an.<br>
				Josef Ganz sendiri mengembangkan Standard Superior (dan di iklan disebutnya "Mobil Rakyat Jerman" (German
				Volkswagen)).[3][rujukan rusak] Selain itu, di Cekoslowakia, Hans Ledwinka juga mengembangkan Tatra T77, 
				mobil yang sangat populer di kalangan elit Jerman. Mobil ini belakangan menjadi semakin kecil dan harganya 
				semakin terjangkau. Tapi, sampai tahun 1933, banyak dari proyek tersebut masih dalam pengembangan atau produksi
				tahap awal. Adolf Hitler menyatakan akan membuat sebuah “mobil rakyat” (Volkswagen). Hitler menginginkan 
				sebuah mobil standar berkapasitas 2 dewasa dan 3 anak dan berkecepatan 100 km/jam (62 mil/jam). Mobil rakyat
				ini nantinya akan tersedia bagi rakyat Nazi dengan harga 990 Reichsmark, seharga sebuah motor kecil pada saat
				itu (pendapatan rata-rata penduduk saat itu sekitar 32 Reichsmark per minggu).[4]<br>
				Insinyur yang terpilih untuk mengembangkan projek mobil ini adalah Ferdinand Porsche. Pada saat itu Porsche 
				sudah menjadi desainer Mercedes 170H dan juga pernah bekerja pada Steyr untuk beberapa saat akhir 1920an. 
				Porsche membuka studio desain pertamanya dan kemudian membuat proyek "Auto für Jedermann" (mobil untuk semua)
				dengan NSU dan Zündapp, keduanya merupakan pabrikan motor. 22 Juni 1934, Dr. Ferdinand Porsche setuju untuk 
				membuatkan mobil rakyat tersebut untuk Hitler.<br>
