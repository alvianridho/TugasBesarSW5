			  <p align="justify">	<b>Audi AG</b> adalah sebuah produsen mobil asal Jerman yang menjual mobil di berbagai macam 
			    kelas dan harga untuk kalangan menengah ke atas.[4] Audi juga merupakan pemilik merek Ducati dan Lamborghini. 
			    Kendaraan Audi diproduksi di 7 pabrik di seluruh dunia, sedangkan merek Ducati dan Lamborghini masing-masing 
			    memiliki satu pabrik yang terletak di Italia. Kantor pusat Audi berada di Ingolstadt, Bavaria, Jerman.
			    Perusahaan ini dimiliki penuh (99,5%) oleh Grup Volkswagen sejak tahun 1966, setelah sebelumnya dibeli dari 
			    Daimler-Benz.[5] Kelahiran kembali Audi ditandai dengan peluncuran Audi F103 pada tahun 1965.</p>
			
			<h5 class="title-d" >Sejarah</h5>
				<p align="justify">Tanggal 14 November 1899, August Horch (1868–1951) mendirikan sebuah perusahaan bernama A.
				Horch & Cie. di distrik Ehrenfeld, Cologne, tetapi karena adanya pertengkaran di antara dirinya dan dewan 
				pengawas perusahaan, Horch harus mengundurkan diri. Beberapa tahun kemudian, Horch mendirikan perusahaan kedua
				bernama Horch Automobil-Werke GmbH tahun 1909. Mobil pertama ciptaan Audi, Audi Tipe A 10 bertenaga 22 hp 
				Sport-Phaeton, diproduksi tahun 1910 di Zwickau.[10]
				Tahun 1909 pula, Horch dipaksa keluar dari perusahaan yang ia dirikan sendiri.[11] Lalu ia mendirikan 
				perusahaan baru lagi di Zwickau tetapi tetap memakai nama Horch. Partner lamanya menggugat dia karena 
				pelanggaran merk dagang. Mahkamah Agung Jerman (Reichsgericht)[12] akhirnya memutuskan kalau merk Horch
				dipunyai oleh perusahaanya yang dulu.[11]<br>
				August Horch akhirnya dilarang untuk memakai nama keluarga untuk bisnis perusahaan barunya. Ia pun 
				mengadakan pertemuan dengan teman bisnisnya, Paul dan Franz Fikentscher. Di apartemen Franz Fikentscher, 
				mereka mendiskusikan nama apa yang cocok untuk perusahaan baru mereka itu. Ceritanya, selagi pertemuan 
				itu pula, anak laki-laki Franz sedang belajar bahasa Latin di pojok ruangan itu. Beberapa kali ia terlihat ingin mengatakan seseatu tetapi ia terus saja belajar sampai akhirnya berkata: "Ayah– audiatur et altera pars... bukankah lebih baik disebut audi saja daripada horch?"[13] ("Horch!" dalam bahasa Jerman artinya mendengarkan, dan dalam bahasa Latin adalah "Audi".) Segera, ide ini diterima dengan antusias seluruh orang yang ada disitu.[14] Mobil pertama Audi, Audi Tipe B, 10/28PS diluncurkan tahun 1910.[15]
				August Horch meninggalkan perusahaan ini tahun 1920 karena diangkat menjadi Menteri Transportasi, tetapi 
				masih aktif di perusahaan sebagai dewan pengawas. Tahun 1921, Audi menjadi perusahaan otomotif Jerman 
				pertama yang meluncurkan mobilnya dalam versi setir kiri.<br>
