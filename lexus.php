			  <h5 class="title-d" >Deskripsi Singkat</h5>
			  <p align="justify">	<b>Lexus</b> (Jepang: レクサス) adalah sebuah merek mobil mewah yang digunakan oleh Toyota Motor Corporation di Amerika Utara, Timur Tengah, Eropa, Australia, Asia (dipasarkan di Jepang pada 2005) dan Selandia Baru. Lexus pertama kali diperkenalkan tahun 1989 di Amerika Serikat, dan sekarang menjadi merek dengan penjualan mobil mewah terbesar dari Jepang. Pada tahun 2006, Lexus dijual di 70 negara di seluruh dunia. Kantor Pusat Lexus berada di Toyota, Aichi, Jepang.
					Project Lexus dimulai pada tahun 1983, sebuah tim besar terdiri dari teknisi terbaik dari Toyota Motor 
					Corporation, berusaha untuk membuat sedan yang bisa mengalahkan sedan terbaik dunia. Hasilnya adalah 
					Lexus LS 400 yang diperkenalkan pada North American International Auto Show tahun 1989.<Br>
					Merek ini terutama bersaing dengan merek mewah tradisional Jerman seperti Audi, BMW, Jaguar, dan 
					Mercedes-Benz. Sejak tahun 2000-an, Lexus mulai berekspansi ke pasar global di luar Amerika Serikat.
					Lexus diperkenalkan ke Jepang pada tahun 2005. Lalu menyusul secara global pada tahun tahun berikutnya,
					termasuk Indonesia pada tahun 2007.<br>
					Lexus terinspirasi dari suksesnya Toyota Supra dan Toyota Cressida. Keduanya merupakan mobil berpenggerak 
					roda belakang dengan mesin M berperforma tinggi.<br>
								
			<h5 class="title-d" >Sejarah</h5>
				<p align="justify">Tahun 1983, Presiden Direktur Toyota Motor Corporation Eiji Toyoda membuat sebuah rapat 
					rahasia di antara para eksekutif Toyota. Ia bertanya, “Bisakah kita membuat sebuah mobil mewah terbaik di 
					dunia?”[1] Pertanyaan Eiji ini membuat Toyota memulai sebuah proyek super-rahasia yang diberi kode F1 
					(“Flagship” (Unggulan) + “No. 1 vehicle” (Mobil No.1)).[2] Proyek ini (yang produk pertamanya Lexus LS 400) 
					bertujuan untuk menambah jajaran produk Toyota, sebuah pijakan untuk memasuki segmen premium dan menambah
					pasar baru.[3] Proyek F1 ini ditargetkan akan mengikuti kesuksesan mobil sport Toyota Supra dan sedan Toyota
					Cressida.[4] Sampai tahun 1980-an, sedan terbesar yang pernah dibuat Toyota adalah Toyota Century, yang 
					dibuat terbatas tahun 1960-an menggunakan mesin V8 dan Toyota Crown yang menggunakan mesin 6 segaris.[5][6]
					Century sendiri dibuat untuk pasar Jepang saja, bukan untuk diekspor. Sedan baru ini ditargetkan untuk menjadi 
					sebuah sedan yang dipasarkan secara global dengan mesin V8.<br>
					Salah satu faktor lain yang semakin membuat Toyota tertarik untuk meluncurkan merek mobil mewah ini 
					adalah peluncuran merek Acura oleh Honda tahun 1986.[7] Setahun kemudian, pabrikan Jepang lain, Nissan, 
					juga berencana untuk meluncurkan merek mobil mewahnya,Infiniti,[8] yang akhirnya terealisasi dengan 
					munculnya Infiniti Q45 tahun 1990.[9] Mazda juga tertarik untuk membuat merek lain dengan nama Amati,
					namun seiring berjalannya waktu rencana ini akhirnya dibatalkan.[8]<br>
					Agar lebih mengetahui profil para orang kaya di Amerika, Toyota mengirimkan para penelitinya ke Amerika 
					Serikat tahun 1985. Dari hasil penelitian itu, sebuah merek baru harus diciptakan, yang terpisah sama 
					sekali dari merek Toyota.[10].<br>
